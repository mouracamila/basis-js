/* 
    Arrow function

    Descrição
    
    Também podemos escrever funções expression de uma maneira mais compacta, 
    desta maneira: () => { código a ser executado }, chamada de Arrow Function.

    geralmente atribuida a uma constante

*/

const sayMyName = (name) => {
    console.log(name)
}

sayMyName('Heisenberg')